//IF
//Memerluksn nilai bool (dalam oprator logical atau comparison)
//Menjalankan bagian proses jika nilai bool berniali true

void main() {
  var usia = 18;
  if (usia < 20) {
    print('Remaja');
  } else {
    print('tidak memiliki golongan');
  }
}

//jika usia kurang dari 20, maka ditampilkan remaja pada layar

//jika tidak, maka ditampilkan tidak memiliki golongan pada layar